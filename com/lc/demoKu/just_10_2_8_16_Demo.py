# 获取用户输入十进制数
dec = int(input("输入数字："))

print("十进制数为：", dec)
print("转换为二进制为：", bin(dec))
print("转换为八进制为：", oct(dec))
print("转换为十六进制为：", hex(dec))

# 控制台示例
# 输入数字：104
# 十进制数为： 104
# 转换为二进制为： 0b1101000
# 转换为八进制为： 0o150
# 转换为十六进制为： 0x68


# 输入数字：64
# 十进制数为： 64
# 转换为二进制为： 0b1000000
# 转换为八进制为： 0o100
# 转换为十六进制为： 0x40